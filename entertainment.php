$('#menulock').click(function(){
	    
		$('#menunav').toggle();
	});
	if($(window).width()>767){
		   $('#menulock').hide();
		   $('#menunav').show();
		}else{
		   $('#menulock').show();
		   $('#menunav').hide();
		}
	$(window).resize(function(){
		if($(this).width()>767){
		   $('#menulock').hide();
		   $('#menunav').show();
		}else{
		   $('#menulock').show();
		   $('#menunav').hide();
		}
	});
    var nav = $('#header');
    var navHomeY = nav.offset().top;
    var isFixed = false;
    var $w = $(window);
    $w.scroll(function() {
        var scrollTop = $w.scrollTop();
        var shouldBeFixed = scrollTop > navHomeY;
        if (shouldBeFixed && !isFixed) {
            nav.css({
                position: 'fixed',
                top: 0,
                left: nav.offset().left,
                width: nav.width()
            });
            isFixed = true;
        }
        else if (!shouldBeFixed && isFixed)
        {
            nav.css({
                position: 'static'
            });
            isFixed = false;
        }
    });
</script>
